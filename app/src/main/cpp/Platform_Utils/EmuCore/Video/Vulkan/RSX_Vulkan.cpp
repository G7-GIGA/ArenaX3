void VulkanRenderer::cleanup() {
    if (pImpl->device != VK_NULL_HANDLE) {
        vkDeviceWaitIdle(pImpl->device);
        
        pImpl->cleanupSwapChain();
        
        for (size_t i = 0; i < MAX_FRAMES_IN_FLIGHT; i++) {
            vkDestroySemaphore(pImpl->device, pImpl->renderFinishedSemaphores[i], nullptr);
            vkDestroySemaphore(pImpl->device, pImpl->imageAvailableSemaphores[i], nullptr);
            vkDestroyFence(pImpl->device, pImpl->inFlightFences[i], nullptr);
        }
        
        vkDestroyCommandPool(pImpl->device, pImpl->commandPool, nullptr);
        
        vkDestroyDescriptorPool(pImpl->device, pImpl->descriptorPool, nullptr);
        vkDestroyDescriptorSetLayout(pImpl->device, pImpl->descriptorSetLayout, nullptr);
        
        vkDestroySampler(pImpl->device, pImpl->textureSampler, nullptr);
        vkDestroyImageView(pImpl->device, pImpl->textureImageView, nullptr);
        vkDestroyImage(pImpl->device, pImpl->textureImage, nullptr);
        vkFreeMemory(pImpl->device, pImpl->textureImageMemory, nullptr);
        
        for (size_t i = 0; i < pImpl->swapChainImages.size(); i++) {
            vkDestroyBuffer(pImpl->device, pImpl->uniformBuffers[i], nullptr);
            vkFreeMemory(pImpl->device, pImpl->uniformBuffersMemory[i], nullptr);
        }
        
        vkDestroyBuffer(pImpl->device, pImpl->indexBuffer, nullptr);
        vkFreeMemory(pImpl->device, pImpl->indexBufferMemory, nullptr);
        vkDestroyBuffer(pImpl->device, pImpl->vertexBuffer, nullptr);
        vkFreeMemory(pImpl->device, pImpl->vertexBufferMemory, nullptr);
        
        vkDestroyPipeline(pImpl->device, pImpl->graphicsPipeline, nullptr);
        vkDestroyPipelineLayout(pImpl->device, pImpl->pipelineLayout, nullptr);
        vkDestroyRenderPass(pImpl->device, pImpl->renderPass, nullptr);
        
        vkDestroyDevice(pImpl->device, nullptr);
    }
    
    if (pImpl->debugMessenger != VK_NULL_HANDLE) {
        auto func = (PFN_vkDestroyDebugUtilsMessengerEXT)vkGetInstanceProcAddr(pImpl->instance, "vkDestroyDebugUtilsMessengerEXT");
        if (func) {
            func(pImpl->instance, pImpl->debugMessenger, nullptr);
        }
    }
    
    if (pImpl->surface != VK_NULL_HANDLE) {
        vkDestroySurfaceKHR(pImpl->instance, pImpl->surface, nullptr);
    }
    
    if (pImpl->instance != VK_NULL_HANDLE) {
        vkDestroyInstance(pImpl->instance, nullptr);
    }
}

void VulkanRenderer::beginFrame() {
    vkWaitForFences(pImpl->device, 1, &pImpl->inFlightFences[pImpl->currentFrame], VK_TRUE, UINT64_MAX);
    
    uint32_t imageIndex;
    VkResult result = vkAcquireNextImageKHR(pImpl->device, pImpl->swapChain, UINT64_MAX, 
                                            pImpl->imageAvailableSemaphores[pImpl->currentFrame], 
                                            VK_NULL_HANDLE, &imageIndex);
    
    if (result == VK_ERROR_OUT_OF_DATE_KHR) {
        pImpl->recreateSwapChain();
        return;
    } else if (result != VK_SUCCESS && result != VK_SUBOPTIMAL_KHR) {
        throw std::runtime_error("failed to acquire swap chain image!");
    }
    
    pImpl->updateUniformBuffer(pImpl->currentFrame);
    
    vkResetFences(pImpl->device, 1, &pImpl->inFlightFences[pImpl->currentFrame]);
    
    vkResetCommandBuffer(pImpl->commandBuffers[imageIndex], 0);
    pImpl->recordCommandBuffer(pImpl->commandBuffers[imageIndex], imageIndex);
    
    VkSubmitInfo submitInfo{};
    submitInfo.sType = VK_STRUCTURE_TYPE_SUBMIT_INFO;
    
    VkSemaphore waitSemaphores[] = {pImpl->imageAvailableSemaphores[pImpl->currentFrame]};
    VkPipelineStageFlags waitStages[] = {VK_PIPELINE_STAGE_COLOR_ATTACHMENT_OUTPUT_BIT};
    submitInfo.waitSemaphoreCount = 1;
    submitInfo.pWaitSemaphores = waitSemaphores;
    submitInfo.pWaitDstStageMask = waitStages;
    submitInfo.commandBufferCount = 1;
    submitInfo.pCommandBuffers = &pImpl->commandBuffers[imageIndex];
    
    VkSemaphore signalSemaphores[] = {pImpl->renderFinishedSemaphores[pImpl->currentFrame]};
    submitInfo.signalSemaphoreCount = 1;
    submitInfo.pSignalSemaphores = signalSemaphores;
    
    if (vkQueueSubmit(pImpl->graphicsQueue, 1, &submitInfo, pImpl->inFlightFences[pImpl->currentFrame]) != VK_SUCCESS) {
        throw std::runtime_error("failed to submit draw command buffer!");
    }
    
    VkPresentInfoKHR presentInfo{};
    presentInfo.sType = VK_STRUCTURE_TYPE_PRESENT_INFO_KHR;
    presentInfo.waitSemaphoreCount = 1;
    presentInfo.pWaitSemaphores = signalSemaphores;
    
    VkSwapchainKHR swapChains[] = {pImpl->swapChain};
    presentInfo.swapchainCount = 1;
    presentInfo.pSwapchains = swapChains;
    presentInfo.pImageIndices = &imageIndex;
    
    result = vkQueuePresentKHR(pImpl->presentQueue, &presentInfo);
    
    if (result == VK_ERROR_OUT_OF_DATE_KHR || result == VK_SUBOPTIMAL_KHR || pImpl->framebufferResized) {
        pImpl->framebufferResized = false;
        pImpl->recreateSwapChain();
    } else if (result != VK_SUCCESS) {
        throw std::runtime_error("failed to present swap chain image!");
    }
    
    pImpl->currentFrame = (pImpl->currentFrame + 1) % MAX_FRAMES_IN_FLIGHT;
}

