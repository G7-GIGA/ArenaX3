    void xorObfuscate(uint8_t* data, size_t size, const std::vector<uint8_t>& xorKey) {
        for (size_t i = 0; i < size; i++) {
            data[i] ^= xorKey[i % xorKey.size()] ^ XOR_KEYSTREAM[i & 0xFF];
        }
    }
    
    std::vector<uint8_t> deriveKeyFromSeed(const std::string& seed) {
        std::vector<uint8_t> key(16, 0);
        for (size_t i = 0; i < seed.size() && i < 16; i++) {
            key[i] = static_cast<uint8_t>(seed[i]) ^ XOR_KEYSTREAM[(i * 7) & 0xFF];
        }
        for (int i = 0; i < 1000; i++) {
            for (size_t j = 0; j < key.size(); j++) {
                key[j] = key[j] ^ XOR_KEYSTREAM[(key[(j+1) % key.size()] + i) & 0xFF];
            }
        }
        return key;
    }
    
