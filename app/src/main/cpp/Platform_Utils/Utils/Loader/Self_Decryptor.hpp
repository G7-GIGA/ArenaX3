// Custom SHA-1 like hash for integrity (simplified, 160-bit)
class IntegrityHash {
public:
    static std::vector<uint8_t> compute(const uint8_t* data, size_t size) {
        uint32_t h0 = 0x67452301, h1 = 0xEFCDAB89, h2 = 0x98BADCFE, h3 = 0x10325476, h4 = 0xC3D2E1F0;
        
        std::vector<uint8_t> padded = padData(data, size);
        for (size_t i = 0; i < padded.size(); i += 64) {
            uint32_t w[80];
            for (int t = 0; t < 16; t++) {
                w[t] = (padded[i + t*4] << 24) | (padded[i + t*4 + 1] << 16) |
                       (padded[i + t*4 + 2] << 8) | padded[i + t*4 + 3];
            }
            for (int t = 16; t < 80; t++) {
                w[t] = rotl(w[t-3] ^ w[t-8] ^ w[t-14] ^ w[t-16], 1);
            }
            
            uint32_t a = h0, b = h1, c = h2, d = h3, e = h4;
            for (int t = 0; t < 80; t++) {
                uint32_t f, k;
                if (t < 20) { f = (b & c) | ((~b) & d); k = 0x5A827999; }
                else if (t < 40) { f = b ^ c ^ d; k = 0x6ED9EBA1; }
                else if (t < 60) { f = (b & c) | (b & d) | (c & d); k = 0x8F1BBCDC; }
                else { f = b ^ c ^ d; k = 0xCA62C1D6; }
                
                uint32_t temp = rotl(a, 5) + f + e + k + w[t];
                e = d; d = c; c = rotl(b, 30); b = a; a = temp;
            }
            h0 += a; h1 += b; h2 += c; h3 += d; h4 += e;
        }
        
        std::vector<uint8_t> hash(20);
        for (int i = 0; i < 4; i++) hash[i] = (h0 >> (24 - i*8)) & 0xFF;
        for (int i = 0; i < 4; i++) hash[4+i] = (h1 >> (24 - i*8)) & 0xFF;
        for (int i = 0; i < 4; i++) hash[8+i] = (h2 >> (24 - i*8)) & 0xFF;
        for (int i = 0; i < 4; i++) hash[12+i] = (h3 >> (24 - i*8)) & 0xFF;
        for (int i = 0; i < 4; i++) hash[16+i] = (h4 >> (24 - i*8)) & 0xFF;
        
        return hash;
    }
    
private:
    static uint32_t rotl(uint32_t x, int n) { return (x << n) | (x >> (32 - n)); }
    static std::vector<uint8_t> padData(const uint8_t* data, size_t size) {
        std::vector<uint8_t> padded(data, data + size);
        padded.push_back(0x80);
        while ((padded.size() % 64) != 56) padded.push_back(0x00);
        uint64_t bitlen = size * 8;
        for (int i = 7; i >= 0; i--) padded.push_back((bitlen >> (i * 8)) & 0xFF);
        return padded;
    }
};

